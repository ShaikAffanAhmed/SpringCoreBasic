package CoreJava;

public class StarPattern {

	public static void main(String[] args) {
		int i,j;
		//for loop Star(*) Pattern 
	       for (i=1;i<=5;i++)  //rows
	       {
	    	   for (j=1;j<=10;j++)  //column
	    	   {
	    		       System.out.print("* ");
	    	       }
	    	             System.out.println("\n");
	           }
    	}	
}
